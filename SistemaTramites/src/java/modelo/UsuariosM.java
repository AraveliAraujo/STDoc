package modelo;

public class UsuariosM {

    private int CodUsu, CodUbig;
    private String DniUsu, NomUsu, ApeUsu, EmailUsu, CelUsu, DirecUsu;

    public int getCodUsu() {
        return CodUsu;
    }

    public void setCodUsu(int CodUsu) {
        this.CodUsu = CodUsu;
    }

    public int getCodUbig() {
        return CodUbig;
    }

    public void setCodUbig(int CodUbig) {
        this.CodUbig = CodUbig;
    }

    public String getDniUsu() {
        return DniUsu;
    }

    public void setDniUsu(String DniUsu) {
        this.DniUsu = DniUsu;
    }

    public String getNomUsu() {
        return NomUsu;
    }

    public void setNomUsu(String NomUsu) {
        this.NomUsu = NomUsu;
    }

    public String getApeUsu() {
        return ApeUsu;
    }

    public void setApeUsu(String ApeUsu) {
        this.ApeUsu = ApeUsu;
    }

    public String getEmailUsu() {
        return EmailUsu;
    }

    public void setEmailUsu(String EmailUsu) {
        this.EmailUsu = EmailUsu;
    }

    public String getCelUsu() {
        return CelUsu;
    }

    public void setCelUsu(String CelUsu) {
        this.CelUsu = CelUsu;
    }

    public String getDirecUsu() {
        return DirecUsu;
    }

    public void setDirecUsu(String DirecUsu) {
        this.DirecUsu = DirecUsu;
    }
    
    
}
