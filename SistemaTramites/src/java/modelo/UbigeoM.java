package modelo;

public class UbigeoM {

    private int CodUbig;
    private String Dpto, Prov, Dist;

    public int getCodUbig() {
        return CodUbig;
    }

    public void setCodUbig(int CodUbig) {
        this.CodUbig = CodUbig;
    }

    public String getDpto() {
        return Dpto;
    }

    public void setDpto(String Dpto) {
        this.Dpto = Dpto;
    }

    public String getProv() {
        return Prov;
    }

    public void setProv(String Prov) {
        this.Prov = Prov;
    }

    public String getDist() {
        return Dist;
    }

    public void setDist(String Dist) {
        this.Dist = Dist;
    }
    
    
}
